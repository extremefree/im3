#include <stdio.h>
#include <string.h>
#include <stdint.h>
void x25519(uint8_t*,const uint8_t*,const uint8_t*);
void x25519_base(uint8_t*,const uint8_t*);
int main(void){
    // RFC 7748 §6.1 向量
    uint8_t k[32]={0x77,0x07,0x6d,0x0a,0x73,0x18,0xa5,0x7d,0x3c,0x16,0xc1,0x72,0x51,0xb2,0x66,0x45,0xdf,0x4c,0x2f,0x87,0xeb,0xc0,0x99,0x2a,0xb1,0x77,0xfb,0xa5,0x1d,0xb9,0x2c,0x2a};
    uint8_t u[32]={9};
    uint8_t want[32]={0x8f,0x40,0xc5,0xad,0xb6,0x8f,0x25,0x62,0x4a,0xe5,0xb2,0x14,0xea,0x76,0x7a,0x6e,0xc9,0x4d,0x82,0x9d,0x3d,0x7b,0x5e,0x1a,0xd1,0xba,0x6f,0x3e,0x21,0x38,0x28,0x5f};
    uint8_t got[32];
    x25519(got,k,u);
    int ok=memcmp(got,want,32)==0;
    printf("x25519(k,9): %s\n",ok?"PASS":"FAIL");
    if(!ok){ for(int i=0;i<32;i++) printf("%02x",got[i]); printf("\n"); }

    // x25519_base
    uint8_t pub[32];
    uint8_t bwant[32]={0x85,0x20,0xf0,0x09,0x89,0x30,0xa7,0x54,0x74,0x8b,0x7d,0xdc,0xb4,0x3e,0xf7,0x5a,0x0d,0xbf,0x3a,0x0d,0x26,0x38,0x1a,0xf4,0xeb,0xa4,0xa9,0x8e,0xaa,0x9b,0x4e,0x6a};
    x25519_base(pub,k);
    ok=memcmp(pub,bwant,32)==0;
    printf("x25519_base(k): %s\n",ok?"PASS":"FAIL");
    if(!ok){ for(int i=0;i<32;i++) printf("%02x",pub[i]); printf("\n"); }
    return 0;
}
